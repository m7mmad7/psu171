public class Nested3
{
    public static void main(String [] args)
    {
        
        for(int i = 1; i <= 100; i++)
        {
            for(int j = 1; j <= 12; j++)
            {
                System.out.println(i + " * " + j + " = " + (i * j));
            }
            System.out.println();
        }
        
        
        
    }
}
