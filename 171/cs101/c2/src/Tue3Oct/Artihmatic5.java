public class Artihmatic5 {

    public static void main(String[] args) {

        // what is modulus
        // 4 % 2   -> 4 / 2 but we look at remainder instead of quotient
       
       /*
          __2___     <- is called quotient
        2 | 4
          | 4
          ------
          | 0        <- is called remainder
        */
       
       //modulus gives remainder
       // division gives quotient
       
        System.out.println(5 / 2); // 2
        System.out.println(5 % 2); // 1
        
        System.out.println(17 % 3); // 2
        
        System.out.println(101 % 10); // 1
        
        System.out.println(5 % 2 + 1.0); //2.0
        
        System.out.println(10 % 3 + 5 % 2); //2
                
        
        
        
    }
}
